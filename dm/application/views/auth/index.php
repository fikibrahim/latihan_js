                        Pesantren Darush Showab dikenal sebagai Pondok Pesantren Salafi yang menonjol dan termashur dalam ilmu alat (tata bahasa dan sastra arab). Hingga kini tercatat ribuan santriawan dan santriawati pernah belajar dan tercatat sebagai
