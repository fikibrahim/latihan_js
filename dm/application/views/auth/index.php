                        Partai Politik dengan menjadi Anggota Dewan Perwakilan Rakyat Daerah (DPRD) Kabupaten Bekasi selama dua periode, yaitu dari tahun 1982 sampai dengan tahun 1992. Berbagai perkembangan dan kemajuan atas hasil perjuangan beliau dilegislatif
                        sangat dirasakan manfaatnya oleh masyarakat, yang nota bene sebelumnya merupakan daerah terpencil dan terisolir. Yang sangat menonjol dirasakan manfaatnya oleh masyarakat adalah perjuangan beliau membuka akses dari keterisoliran
                        dengan membuka akses pendidikan, akses sambungan PLN dan akses jalan .Beliau menunaikan rukun islam kelima naik haji sebanyak tujuh kali mulai tahun 1994. Penghargaan dari Pemerintah dan dari berbagai lembaga dan tokoh pernah diperoleh,
                        diantaranya ditahun 1997 sebuah Perguruan Tinggi di Jakarta menganugerahi beliau Gelar Doktor Kehormatan (Doktor Honoris Causa) serta berbagai kalangan dan tokoh Nasional pernah berkunjung ke Ponpes/Yayasan Pendidikan Islam Darul
                        Mu’allamah diantaranya adalah H. Agung Laksono, Jendral Try Sutrisno, H. Rhoma Irama, KH. AF Ghozali dan lain-lain. Demi meningkatkan dan memperluas pelayanannya terhadap masyarakat dan juga atas dasar respon tantangan zaman yang
