                        KH. Hasan Djuwaeni muda selain sebagai santri juga dipercaya sebagai guru bantu(badal) dalam mengajar dan membimbing santri-santri. Setelah menikah perjalanan keilmuan beliau tidaklah berhenti tapi justru dalam perjalanan selanjutnya
